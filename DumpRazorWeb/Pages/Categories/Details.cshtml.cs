#nullable disable
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using DumpRazorWeb.Data;
using DumpRazorWeb.Model;

namespace DumpRazorWeb.Pages.Categories
{
    public class DetailsModel : PageModel
    {
        private readonly DumpRazorWeb.Data.AppDBContext _context;

        public DetailsModel(DumpRazorWeb.Data.AppDBContext context)
        {
            _context = context;
        }

        public Category Category { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Category = await _context.Categories.FirstOrDefaultAsync(m => m.Id == id);

            if (Category == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
